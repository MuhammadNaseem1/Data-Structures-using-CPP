int insertion_Sort(int array[])
{
	int temp;
	int comp=0;
	
	for(int i=0; i<5-1; i++)
	{
		comp++;
		if((array[i]>array[i+1]))
		{
			comp--;
			if(i==0)
			{
				comp++;
				temp=array[i];
				array[i]=array[i+1];
				array[i+1]=temp;
			}
			else
			{
				int a=i;
			do
			{
				comp++;
				temp=array[a];
				array[a]=array[a+1];
				array[a+1]=temp;
				a--;
			}
			while(a>=0 && array[a]>array[a+1]);
		}
	}
	}
	for(int k=0; k<5; k++)
	{
		cout<<array[k]<<" ";
	}
	cout<<endl;
	cout<<"comparisons are : "<<comp<<endl;
	return comp;
}
